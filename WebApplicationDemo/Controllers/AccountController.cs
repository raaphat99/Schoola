        [HttpPost]
        public IActionResult Register(RegisterViewModel registerData)
        {
            if (ModelState.IsValid)
            {
                User newUser = new User() { Email = registerData.Email, Password = registerData.Password };
                _context.Users.Add(newUser);
                _context.SaveChanges();
                return RedirectToAction("Login");
            }
            ModelState.AddModelError(String.Empty, "Please Enter valid entries!");
            return View(registerData);
        }

        public IActionResult Login()
        {
            return View();
        }

        [HttpPost]
        public async Task<IActionResult> Login(LoginViewModel loginData)
        {
            if (ModelState.IsValid)
            {
                var user = _context.Users.Where(user => user.Email == loginData.Email && user.Password == loginData.Password)?.Include(user => user.Roles)?.FirstOrDefault();
                if (user == null)
                {
                    ModelState.AddModelError(string.Empty, "Invalid Email or Password.");
                    return View(loginData);
                }

                // if user exists
                var userRoles = user.Roles.ToList();
                ClaimsIdentity claimsIdentity = new ClaimsIdentity(CookieAuthenticationDefaults.AuthenticationScheme);
                foreach (var role in userRoles)
                {
                    Claim roleClaim = new Claim(ClaimTypes.Role, role.Type);
                    claimsIdentity.AddClaim(roleClaim);
                }
                ClaimsPrincipal claimsPrincipal = new ClaimsPrincipal(claimsIdentity);
                await HttpContext.SignInAsync(claimsPrincipal);
                return RedirectToAction("Index", "Course");
            }
            ModelState.AddModelError(string.Empty, "Please Enter valid entries!");
            return View(loginData);
        }

        public async Task<IActionResult> Logout()
        {
            await HttpContext.SignOutAsync();
            return RedirectToAction("Index", "Home");
        }

        public IActionResult AccessDenied()
        {
            return View();
